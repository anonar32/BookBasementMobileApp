package com.example.bookbasement_02.Helpers;

public class StringHelper {

    public StringHelper(){

    }
    /**
     *
     * @param param
     * @return int
     */
    public int toInt(String param){
        return Integer.parseInt(param);
    }

    /**
     *
     * @param param
     * @return double
     */
    public double toDouble(String param){
        return Double.parseDouble(param);
    }

}
